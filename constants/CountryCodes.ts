export const getPopularCountries = (): Country[] => {
  return COUNTRIES.filter(country => country.priority).sort((a, b) => (a.priority || 0) - (b.priority || 0));
};

export const getAllCountries = (): Country[] => {
  return COUNTRIES.sort((a, b) => a.name.localeCompare(b.name));
};

export const searchCountries = (query: string): Country[] => {
  if (!query.trim()) return getAllCountries();
  
  const lowercaseQuery = query.toLowerCase();
  return COUNTRIES.filter(country => 
    country.name.toLowerCase().includes(lowercaseQuery) ||
    country.dialCode.includes(query) ||
    country.code.toLowerCase().includes(lowercaseQuery)
  ).sort((a, b) => {
    // Prioritize popular countries in search results
    if (a.priority && !b.priority) return -1;
    if (!a.priority && b.priority) return 1;
    if (a.priority && b.priority) return a.priority - b.priority;
    return a.name.localeCompare(b.name);
  });
};

export const getCountryByCode = (code: string): Country | undefined => {
  return COUNTRIES.find(country => country.code === code);
};

export const getCountryByDialCode = (dialCode: string): Country | undefined => {
  return COUNTRIES.find(country => country.dialCode === dialCode);
};